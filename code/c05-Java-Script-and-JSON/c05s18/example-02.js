/**
 * This program prints the string 'Hello world!' 5 times
 */

// initialize loop variable
var MAX_MESSAGES = 5
var count = 0

while (count < MAX_MESSAGES) {
    console.log(count + 1, "Hello world!")
    count = count + 1 // count++
}

/* === Output ====

1 'Hello world!'
2 'Hello world!'
3 'Hello world!'
4 'Hello world!'
5 'Hello world!'

*/
